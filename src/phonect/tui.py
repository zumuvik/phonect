class PairingScreen(Screen):
    """Pairing wizard: generate PC keys, display QR code."""

    def compose(self) -> ComposeResult:
        yield Label("Pairing Wizard", classes="title")
        yield Label(
            "Generate a PC key pair and scan the QR code with the phonect Android app.",
            id="pairing-hint",
        )

        yield Label("PC Name:")
        yield Input(placeholder="my-laptop", id="pc-name")

        with Horizontal():
            yield Button("Generate PC Keys", id="btn-gen-keys", variant="primary")
            yield Button("Show QR Code", id="btn-show-qr", variant="default", disabled=True)

        yield Static("", id="key-status")
        yield Static("", id="pairing-qr", classes="qr-code")
        yield Button("Back", id="btn-back", variant="default")

    @on(Button.Pressed, "#btn-gen-keys")
    async def on_gen_keys(self) -> None:
        """Generate an RSA-4096 key pair in a background thread.

        RSA key generation is CPU-intensive (several seconds), so it runs
        in an executor to keep the UI responsive.
        """
        btn = self.query_one("#btn-gen-keys", Button)
        status = self.query_one("#key-status", Static)
        qr_btn = self.query_one("#btn-show-qr", Button)

        btn.disabled = True
        qr_btn.disabled = True
        status.update("[yellow]Generating RSA-4096 key pair…[/yellow]")

        try:
            # CPU-bound: run in thread executor
            loop = asyncio.get_running_loop()
            kp = await loop.run_in_executor(None, generate_key_pair)

            # Write keys to user-writable directory
            config_dir = USER_CONFIG_DIR
            config_dir.mkdir(parents=True, exist_ok=True)

            priv_path = config_dir / "pc_private.pem"
            pub_path = config_dir / "pc_public.pem"

            priv_path.write_bytes(kp.private_key_pem)
            pub_path.write_bytes(kp.public_key_pem)

            self._pc_kp = kp

            status.update(
                f"[green]✓[/green] Keys generated!\n"
                f"  Private: {priv_path} ({len(kp.private_key_pem)} bytes)\n"
                f"  Public:  {pub_path} ({len(kp.public_key_pem)} bytes)\n"
                f"  Fingerprint: {kp.public_key_fingerprint[:32]}…"
            )
            qr_btn.disabled = False
        except Exception as e:
            status.update(f"[red]Error generating keys: {e}[/red]")
            self.notify(f"Key generation failed: {e}", severity="error")
        finally:
            btn.disabled = False

    @on(Button.Pressed, "#btn-show-qr")
    def on_show_qr(self) -> None:
        """Show pairing QR code (synchronous — fast path)."""
        qr_widget = self.query_one("#pairing-qr", Static)

        if not hasattr(self, "_pc_kp"):
            # Try loading from files
            pub_path = USER_CONFIG_DIR / "pc_public.pem"
            if pub_path.exists():
                try:
                    pub = load_public_key(pub_path.read_bytes())
                    fp = fingerprint_from_public_key(pub)
                    pub_pem = public_key_to_pem(pub)

                    self._pc_kp = KeyPair(
                        private_key_pem=b"",
                        public_key_pem=pub_pem.encode(),
                        public_key_fingerprint=fp,
                    )
                except Exception as e:
                    qr_widget.update(f"[red]Error reading key: {e}[/red]")
                    self.notify(f"Cannot load public key: {e}", severity="error")
                    return
            else:
                qr_widget.update("[red]No keys found. Click 'Generate PC Keys' first.[/red]")
                return

        pc_name = self.query_one("#pc-name", Input).value or socket.gethostname()
        pc_ip = discover_local_ip()
        pc_port = 9876

        try:
            pairing_data = make_pairing_token(
                pc_name=pc_name,
                pc_ip=pc_ip,
                pc_port=pc_port,
                pc_fingerprint=self._pc_kp.public_key_fingerprint,
                pc_public_key_pem=self._pc_kp.public_key_pem.decode(),
            )

            qr_art = render_ascii_qr(pairing_data)

            info = (
                f"\n[b]PC Name:[/b] {pc_name}\n"
                f"[b]IP:[/b] {pc_ip}:{pc_port}\n"
                f"[b]Fingerprint:[/b] {self._pc_kp.public_key_fingerprint[:32]}…\n"
                f"\nScan this QR code with the phonect Android app:\n"
            )
            qr_widget.update(info + qr_art)
        except Exception as e:
            qr_widget.update(f"[red]Error generating QR code: {e}[/red]")
            self.notify(f"QR generation failed: {e}", severity="error")

    @on(Button.Pressed, "#btn-back")
    def on_back(self) -> None:
        self.app.pop_screen()


