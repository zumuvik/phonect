# ======================================================================
# Constants
# ======================================================================

# Path where the NixOS module writes the daemon config
DAEMON_CONFIG_PATH = Path("/etc/phonect/config.toml")
# User-writable directory for keys generated by the TUI
USER_CONFIG_DIR: Path = (
    Path(os.environ.get("XDG_CONFIG_HOME", Path.home() / ".config")) / "phonect"
)


# ======================================================================
# Pairing token helpers
# ======================================================================


def make_pairing_token(
    pc_name: str,
    pc_ip: str,
    pc_port: int,
    pc_fingerprint: str,
    pc_public_key_pem: str,
) -> str:
    """Build a JSON token for QR-code pairing."""
    return json.dumps({
        "pc_name": pc_name,
        "pc_ip": pc_ip,
        "pc_port": pc_port,
        "pc_fingerprint": pc_fingerprint,
        "pc_public_key": pc_public_key_pem,
    }, separators=(",", ":"))


def render_ascii_qr(data: str) -> str:
    """Render a QR code as ASCII art (pure Python, no Pillow needed)."""
    import qrcode

    qr = qrcode.QRCode(
        version=None,          # auto-detect
        error_correction=qrcode.constants.ERROR_CORRECT_M,
        box_size=2,
        border=2,
    )
    qr.add_data(data)
    qr.make(fit=True)

    # Convert matrix to ASCII
    try:
        matrix = qr.get_matrix()
        lines = []
        for row in matrix:
            line = "".join("  " if cell else "██" for cell in row)
            lines.append(line)
        return "\n".join(lines)
    except Exception:
        # Pure text fallback
        return qr.make_image().to_text(quiet_zone=2)


def discover_local_ip() -> str:
    """Heuristic: find the LAN IP by connecting to a non-routable address."""
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as s:
            s.settimeout(0.5)
            s.connect(("192.168.1.1", 1))
            return s.getsockname()[0]
    except Exception:
        return "127.0.0.1"


# ======================================================================
# Helpers
# ======================================================================


def _load_daemon_config() -> DaemonConfig:
    """Load the config the daemon actually uses.

    Tries ``/etc/phonect/config.toml`` first (NixOS-generated), falls
    back to the default user path.
    """
    if DAEMON_CONFIG_PATH.exists():
        return load_config(DAEMON_CONFIG_PATH)
    return load_config()


