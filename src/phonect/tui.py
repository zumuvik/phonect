class LogsScreen(Screen):
    """Real-time journalctl log viewer for phonect daemon.

    Uses ``journalctl --user -n 50 -f -u phonect`` to tail logs from the
    user-systemd service.  The subprocess is driven through
    ``asyncio.create_subprocess_exec`` so the main event loop never blocks.
    """

    def compose(self) -> ComposeResult:
        yield Label("Daemon Logs", classes="title")
        yield RichLog(id="log-view", highlight=True, markup=True, max_lines=500)
        yield Horizontal(
            Button("Pause", id="btn-pause"),
            Button("Clear", id="btn-clear"),
            Button("Back", id="btn-back"),
        )

    def on_mount(self) -> None:
        self._log_view = self.query_one("#log-view", RichLog)
        self._paused = False
        self._log_view.write("[dim]Connecting to journalctl…[/dim]")
        self._tail_task = asyncio.create_task(self._tail_journalctl())

    async def _tail_journalctl(self) -> None:
        """Tail ``journalctl --user -n 50 -f -u phonect`` asynchronously.

        The subprocess is created with ``asyncio.create_subprocess_exec``
        so reading stdout never blocks the UI thread.  All errors are
        caught and displayed inline (with a ``self.app.notify()`` banner).
        """
        try:
            proc = await asyncio.create_subprocess_exec(
                "journalctl",
                "--user",
                "-n", "50",
                "-f",
                "-u", "phonect",
                "--no-pager",
                "--output=short-iso",
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
            )
        except FileNotFoundError:
            msg = "[yellow]journalctl not available (not Linux/systemd)[/yellow]"
            self._log_view.write(msg)
            self.notify("journalctl not found — not running on Linux/systemd?", severity="error")
            return
        except Exception as e:
            msg = f"[red]Failed to start journalctl: {e}[/red]"
            self._log_view.write(msg)
            self.notify(f"journalctl error: {e}", severity="error")
            return

        self._log_view.write("[green]✓[/green] Connected, waiting for logs…")
        self._log_view.clear()

        # Read loop — asyncio buffered IO, never blocks
        while True:
            try:
                line = await asyncio.wait_for(proc.stdout.readline(), timeout=30.0)
            except asyncio.TimeoutError:
                # No output for 30s — not an error, just keep waiting
                continue
            except Exception as e:
                self._log_view.write(f"[red]Log read error: {e}[/red]")
                self.notify(f"Log stream error: {e}", severity="error")
                break

            if not line:
                # EOF — journalctl exited
                break

            if not self._paused:
                try:
                    self._log_view.write(line.decode("utf-8", errors="replace").rstrip())
                except Exception as e:
                    self._log_view.write(f"[red]Log decode error: {e}[/red]")

        # If we get here the subprocess ended
        self._log_view.write("[dim]journalctl process ended[/dim]")

    @on(Button.Pressed, "#btn-pause")
    def on_pause(self) -> None:
        self._paused = not self._paused
        btn = self.query_one("#btn-pause", Button)
        btn.label = "Resume" if self._paused else "Pause"

    @on(Button.Pressed, "#btn-clear")
    def on_clear(self) -> None:
        self._log_view.clear()

    @on(Button.Pressed, "#btn-back")
    def on_back(self) -> None:
        self._tail_task.cancel()
        self.app.pop_screen()


